#pedindo a base do triangulo
base=float(input("Digite o valor da base do triangulo: "))

#pedindo a altura do triangulo
altura=float(input("Digite a altura do triangulo: "))

#calculando a sua área
area=(base*altura)/2

#exibindo a área do triangulo
print(f"o triangulo com base {base} e altura {altura} tem área igual a {area}")