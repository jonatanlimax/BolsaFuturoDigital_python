#pedindo o primeiro numero
num1=int(input("digite o primeiro numero: "))

#pedindo o segundo número
num2=int(input("digite o segundo numero: "))

#calculando a divisão inteira
inteira=num1//num2

#calculando o resto da divisao
resto = num1%num2

#exibindo o resultado
print(f"a divisão inteira de {num1} e {num2} é igual a {inteira}, e o resto da divisão é {resto}")